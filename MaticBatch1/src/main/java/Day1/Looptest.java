package Day1;

public class Looptest {
	public static void main(String[] args) {
		for(int i=10; i>0; i--) {
			System.out.println(i);
		}
		 int i=10;
	while(i>0) {
		System.out.println(i);
		i--;
	}
	int k=10;
	System.out.println("Value of k");
	do {
		System.out.println(k);
		k--;
	}while(k>0);
	}
}
